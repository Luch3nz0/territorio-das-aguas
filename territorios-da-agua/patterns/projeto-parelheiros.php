		<h3 class="wp-block-heading">Projeto Piloto Parelheiros</h3>
		<!-- /wp:heading -->

		<!-- wp:paragraph -->
		<p>A Bacia do Ribeirão Parelheiros está localizada no extremo sul do Município de São Paulo, nas Subprefeituras de Parelheiros e Capela do Socorro. Seu principal curso d’água dá nome à bacia, também conhecido como Ribeirão Caulim.</p>
		<!-- /wp:paragraph -->

		<!-- wp:paragraph -->
		<p>Essa bacia hidrográfica é caracterizada por receber água de transposição da represa Billings e contribuir para a represa Guarapiranga. Está inserida na Área de Proteção e Recuperação de Mananciais Sul, responsável pelo abastecimento de 30% da população da Região Metropolitana de São Paulo. A poluição difusa proveniente de fontes internas externas à bacia é um dos principais desafios que impactam seu principal curso d’água, bem como os demais afluentes.</p>
		<!-- /wp:paragraph -->

		<!-- wp:paragraph -->
		<p>Além da sua importância hídrica, a Bacia do Ribeirão Parelheiros se destaca pela rica biodiversidade, abrigando remanescentes da Mata Atlântica e várias áreas protegidas, como a Área de Proteção Ambiental Bororé-Colônia, os Parques Naturais Municipais Jaceguava, Itaim e Varginha, e o Parque Linear Ribeirão Caulim (Núcleo São Rafael).</p>
		<!-- /wp:paragraph -->

		<!-- wp:paragraph -->
		<p>Nas últimas décadas, a expansão de construções irregulares reflete a elevada vulnerabilidade social na região, manifestada pela ocupação de áreas de risco e ambientalmente frágeis, o que resulta em uma crescente pressão sobre Áreas de Preservação Permanente e fragmentos de vegetação nativa.</p>
		<!-- /wp:paragraph -->

		<!-- wp:paragraph -->
		<p>Nesse cenário, o tratamento das APPs requer uma mudança de abordagem, a partir de uma perspectiva multidisciplinar e em múltiplas escalas, que extrapole recortes normativos e articule as dimensões socioambientais e de gestão, de maneira a avançar institucionalmente na formulação de políticas públicas integradas, por meio de inovações em técnicas, procedimentos de intervenção e de novos arranjos institucionais, incluindo a gestão participativa. Nesse sentido, nasce a proposta de um Termo de Cooperação composto pela Secretaria Municipal do Verde e do Meio Ambiente (SVMA) e a Secretaria Executiva do Programa Mananciais (SEPM), com o objetivo de desenvolver análises e propostas de interesse do município de São Paulo na Bacia do Ribeirão Parelheiros. A parceria visa integrar propostas para as Áreas De Preservação Permanente dos cursos d'água, tendo em vista os condicionantes e as potencialidades existentes no território, com especial atenção às áreas de maior vulnerabilidade social e ambiental.</p>
		<!-- /wp:paragraph -->

		<!-- wp:paragraph -->
		<p>Enquanto produtos da cooperação, será elaborado um Plano Urbanístico e Ambiental para a Bacia do Ribeirão Parelheiros, subdividido em um diagnóstico territorial, já elaborado, e em propostas de diretrizes e intervenções, em fase de construção, a partir da definição de cenários de urbanização e do mapeamento de pressões, ameaças e oportunidades. A metodologia envolve visitas de campo, oficinas de trabalho com gestores públicos e pesquisadores, além de estudos hidráulicos e hidrológicos, com o objetivo de compreender o território da bacia para propor alternativas que sejam capazes de reduzir os impactos existentes sobre as APPs, promover justiça ambiental e garantir justiça social.</p>
		<!-- /wp:paragraph -->

		<!-- wp:heading {"level":4} -->
		<h4 class="wp-block-heading">Diagnóstico Territorial da Bacia do Ribeirão Parelheiros [detalhamento]</h4>
		<!-- /wp:heading -->

		<!-- wp:paragraph -->
		<p>O diagnóstico territorial mapeou os principais condicionantes e potencialidades urbanístico-ambientais da Bacia do Ribeirão Parelheiros, com foco na conservação e recuperação das Áreas de Preservação Permanente (APPs).</p>
		<!-- /wp:paragraph -->

		<!-- wp:paragraph -->
		<p>A bacia tem histórico de ocupação em área ambientalmente sensível, com impactos sobre seu potencial ambiental e turístico. Nos últimos 15 anos, a expansão urbana se intensificou de forma precarizada, gerando crescente demanda por infraestrutura e saneamento ambiental.</p>
		<!-- /wp:paragraph -->

		<!-- wp:paragraph -->
		<p>Cerca de 83% de sua hidrografia ainda se encontra em estado natural, um dado relevante do ponto de vista da conservação. Nas áreas urbanizadas, porém, os cursos d'água apresentam erosões, descarte de lixo e entulho nas margens, lançamento de esgoto sem tratamento e assoreamento expressivo.</p>
		<!-- /wp:paragraph -->

		<!-- wp:paragraph -->
		<p>Quatro elementos estruturam e condicionam o processo de urbanização na bacia: a ocupação radial, os cursos d'água, os parques existentes e a infraestrutura viária. Essa lógica espacial orienta tanto a análise dos vetores de expansão quanto a proposição de cenários de intervenção.</p>
		<!-- /wp:paragraph -->

		<!-- wp:paragraph -->
		<p>A bacia apresenta Taxa Geométrica de Crescimento Anual (TGCA), entre 2010 e 2022, de 7,7% a.a., muito acima das médias nacional (0,52%) e municipal (0,15%), evidenciando forte expansão e adensamento populacional. O crescimento de favelas em APPs chega a 4,4% a.a. A população é majoritariamente parda (44,1%) e feminina (47,1%), dados que reforçam a centralidade da justiça ambiental como eixo estruturante das políticas para a região.</p>
		<!-- /wp:paragraph -->

		<!-- wp:paragraph -->
		<p>As legislações urbanísticas e ambientais vigentes buscam qualificar a ocupação consolidada, conter a expansão irregular e fomentar atividades compatíveis com a conservação. O crescimento dos núcleos urbanos demandou a demarcação de Zonas Especiais de Interesse Social (ZEIS), e a implantação de parques naturais e urbanos tem sido acionada como instrumento de proteção da biodiversidade.</p>
