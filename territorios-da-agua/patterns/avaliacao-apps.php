		<h2 class="wp-block-heading tda-section-title">Avaliação das Intervenções em APPs</h2>
		<!-- /wp:heading -->

		<!-- wp:paragraph -->
		<p>A Avaliação das Intervenções em Áreas de Preservação Permanente (APPs) integra o Projeto Territórios e foi desenvolvida para reunir e sistematizar informações sobre as intervenções realizadas em APPs urbanas associadas aos cursos d'água do município de São Paulo, entre 2002 e 2024. Este trabalho contribui para a construção do Programa de Conservação e Recuperação de APPs do Município de São Paulo.</p>
		<!-- /wp:paragraph -->

		<!-- wp:paragraph -->
		<p>A proposta surgiu da necessidade de compreender como as APPs urbanas vêm sendo tratadas pelo poder público ao longo do tempo. Assim, considerando os distintos contextos desses territórios e que, possivelmente, parte dessas informações poderiam estar dispersas, a reunião do material em uma única base de dados foi um passo decisivo para entender as intervenções, como elas foram conduzidas e o que as experiências observadas ensinariam para o futuro.</p>
		<!-- /wp:paragraph -->

		<!-- wp:paragraph -->
		<p>Para a construção do levantamento dos dados, foi realizada a revisão bibliográfica sistemática da produção acadêmica sobre as intervenções em APPs urbanas no município de São Paulo, incluindo artigos, dissertações e teses. Em paralelo, foi desenvolvido o levantamento documental de registros institucionais produzidos por diferentes secretarias municipais da Prefeitura de São Paulo (PMSP) com atuação sobre esses territórios, tendo como fonte de dados o portal de notícias da PMSP. Com esse conjunto de informações, totalizando 120 artigos selecionados e 434 notícias do portal da Prefeitura, foi possível sistematizar as intervenções em uma base de dados estruturada, reunindo dados sobre localização, características das ações, órgãos envolvidos e outros elementos registrados nas fontes analisadas.</p>
		<!-- /wp:paragraph -->

		<!-- wp:paragraph -->
		<p>O levantamento permitiu a construção de uma base de dados de intervenções realizadas em APPs urbanas no município. Foram identificadas 274 intervenções associadas a 209 corpos d’água, sendo possível observar a distribuição territorial, os tipos de intervenções realizadas e os órgãos públicos envolvidos em sua implementação. Essa base passou a compor uma das referências do Projeto Territórios da Água e apoia diretamente suas atividades de pesquisa e desenvolvimento.</p>
		<!-- /wp:paragraph -->

		<!-- wp:paragraph -->
		<p>Além da sistematização dos dados, o trabalho resultou em produtos técnicos e científicos que registram e divulgam os resultados obtidos. Entre eles destacam-se o artigo científico<sup>1</sup> apresentado no Encontro Nacional da Associação Nacional de Pós-Graduação e Pesquisa em Planejamento Urbano e Regional (ENANPUR), em 2025, a Nota Técnica sobre as Intervenções em Áreas de Preservação Permanente no Município de São Paulo (2002 -2024) e os subsídios utilizados para o desenvolvimento do projeto e construção do programa de Conservação e Recuperação de APPs.</p>
		<!-- /wp:paragraph -->

		<!-- wp:paragraph -->
		<p>A avaliação, produzida por meio da reunião de dados, ofereceu um panorama das intervenções em APPs no município de São Paulo e ampliou a disponibilidade de informações sobre a atuação pública. Ela descreve ações isoladas, ajuda a revelar padrões, lacunas e possíveis caminhos para a gestão desses territórios, reforçando sua importância para o planejamento urbano e ambiental da cidade.</p>
		<!-- /wp:paragraph -->

		<!-- wp:paragraph -->
		<p><sup>1</sup> <a href="https://editorarealize.com.br/artigo/visualizar/122574">https://editorarealize.com.br/artigo/visualizar/122574</a></p>
		<!-- /wp:paragraph -->

		<!-- wp:heading {"level":3} -->
		<h3 class="wp-block-heading">Resultados Avaliação das Intervenções em APPs [infográficos]</h3>
		<!-- /wp:heading -->

		<!-- wp:paragraph -->
		<p>As obras de macrodrenagem dominaram o período de análise, respondendo por 45,6% do total. Em seguida, vieram as intervenções intersetoriais<sup>2</sup> (33,9%), das quais 56% corresponderam a urbanização de favelas, e as obras de implantação de parques em APPs (16,8%). A Secretaria de Infraestrutura Urbana (SIURB) foi a principal proponente, com 65 intervenções, seguida pela Subprefeituras (SMSUB, 38), SVMA (33) e SEHAB (30). A distribuição territorial concentrou-se nas zonas Leste e Sul, as duas com maior número de intervenções (106 e 104, respectivamente) e também as com maior interface com assentamentos precários e áreas de risco.</p>
		<!-- /wp:paragraph -->

		<!-- wp:paragraph -->
		<p>A análise temporal aponta mudanças significativas no perfil das intervenções. Nos anos 2000, os resultados do Programa 100 Parques marcaram o período com um pico de obras de criação de parques em APPs. Entre 2007 e 2012, os recursos do PAC impulsionaram um aumento expressivo das intervenções intersetoriais. A partir de 2017, houve queda nesse tipo de obra e crescimento das intervenções setoriais de macrodrenagem, sendo essa uma tendência que se acentuou nos últimos anos, com predomínio de obras emergenciais, especialmente a partir de 2022. De modo geral, embora se observe uma transformação no tratamento das APPs no município em relação ao período histórico, ainda persiste a realização de obras públicas setoriais, isto é, sem soluções ambientais, sociais e urbanísticas integradas.</p>
		<!-- /wp:paragraph -->

		<!-- wp:paragraph -->
		<p><sup>2</sup> As obras identificadas como “intersetoriais” dizem respeito às intervenções que abrangem mais de um tipo de obra, como, por exemplo: obra de macrodrenagem associada à parque linear etc.</p>
